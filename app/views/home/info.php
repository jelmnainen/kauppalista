                

<h2>Yleistä</h2>
<p>
    Kauppalista on Johannes Elmnäisen tietokantasovellus. Se tehtiin alunperin 
    Helsingin yliopiston tietojenkäsittelytieteiden laitokselle tietokantojen 
    harjoitustyöksi.
</p>

<p>
    Kauppalistaa Johannes kehittää miten mielii, jossain vaiheessa olisi hienoa  
    toteuttaa Android-sovellus tietokannan päälle. Kahtotaan.
</p>

