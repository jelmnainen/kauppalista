                <h2>
                    Työn vaihe
                </h2>
<p>
    Työssä on pari toimivaa mallia (kauppalista ja ostos),
    joiden ympärille olisi tosin vielä hyvä rakentaa lisäfunktioita.
</p>

<p>
    Koodi on tällä hetkellä muuten mielestäni ihan selkeää, mutta koska ymmärsin MVC:n aluksi vähän hassusti ja sen jälkeen
    vanhemman käpistelijän johdolla ehkä hieman monimutkaisesti, poikkesin kurssilla esitetystä sen verran,
    että pidän modelia lähinnä arranyä jonka täytän servicen luomalla domainilla ja vien viewiin.
</p>
                
                <h2>Työn aihe</h2>
                        
                <p>
                    Kauppalista on tietokantasovellus, joka toimii virtuaalisena muistilistana kauppaostoksista. Yksineläjille sen päätoimintona on nimenomaan toimia mukaan otettavana listana ostoksista. Useamman hengen ruokakunnille ruokalista tarjoaa yhteistoimintomahdollisuuksia, kuten yhteistyössä koottavat ostoslistat,  tarpeiden hankinnan hajauttamiseen ja kulujen kirjanpidon.
                </p>

                